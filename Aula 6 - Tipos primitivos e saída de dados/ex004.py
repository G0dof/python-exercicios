n1 = input('\033[1;33mDigite algo= ')
print('\033[30mO tipo primitivo deste valor é {}'.format(type(n1)))
print('\033[31mSó tem espaços? {}'.format(n1.isspace()))
print('\033[32mÉ um número? {}'.format(n1.isnumeric()))
print('\033[33mÉ alfabético? {}'.format(n1.isalpha()))
print('\033[34mÉ alfanumérico? {}'.format(n1.isalnum()))
print('\033[35mEstá em maiúsculas? {}'.format(n1.isupper()))
print('\033[36mEstá em minúsculas? {}'.format(n1.islower()))
print('\033[37mEstá capitalizado? {}'.format(n1.istitle()))
